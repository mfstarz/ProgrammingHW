#include <iostream>

using namespace std;

int main() {
	cout << "_________________________________________________\n";
	for (int i = 0; i < 6; i++) {
		if (i % 2 == 0) {
			cout << "* * * * * * * * _________________________________\n";
		}
		if (i % 2 != 0) {
			cout << " * * * * * * * *_________________________________\n";
		}
	}
	for (int i = 0; i < 6; i++) {
		cout << "_________________________________________________\n";
	}
	return 0;
}